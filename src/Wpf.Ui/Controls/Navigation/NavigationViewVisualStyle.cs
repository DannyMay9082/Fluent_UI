namespace Wpf.Ui.Controls.Navigation;

/// <summary>
/// Defines constants that specify visual style of the NavigationView.
/// </summary>
public enum NavigationViewVisualStyle
{
    /// <summary>
    /// Standard visual style of NavigationView in Win Ui.
    /// </summary>
    Compact,

    /// <summary>
    /// Wide buttons, with full titles, similar to the settings app for Windows 11.
    /// </summary>
    Wide,

    /// <summary>
    /// Modern, tiled style of navigation, similar to the Windows Store app.
    /// </summary>
    Fluent,
}
